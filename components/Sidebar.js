export default function Sidebar() {
  const pathname = usePathname()
  const router = useRouter()
  const supabase = createClient()

  async function signOut() {
    await supabase.auth.signOut()
    router.push('/login')
    router.refresh()
  }

  function isActive(href) {
    if (href === '/') return pathname === '/'
    return pathname.startsWith(href)
  }

  return (
    <div className="w-56 bg-slate-900 flex flex-col h-full flex-shrink-0 print:hidden">
      {/* Logo */}
      <div className="px-4 py-5 border-b border-slate-800">
        <h1 className="text-white font-bold text-base tracking-tight">Storage Books</h1>
        <p className="text-slate-500 text-xs mt-0.5">Self-Storage Financials</p>
      </div>

      {/* Nav */}
      <nav className="flex-1 px-3 py-4 space-y-0.5 overflow-y-auto">
        {NAV.map((item, i) => {
          if (item.section) {
            return (
              <p key={i} className="text-slate-500 text-xs font-semibold uppercase tracking-wider px-2 pt-4 pb-1">
                {item.section}
              </p>
            )
          }
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-2.5 px-2 py-2 rounded-lg text-sm transition-colors ${
                isActive(item.href)
                  ? 'bg-white text-slate-900 font-medium'
                  : 'text-slate-400 hover:text-white hover:bg-slate-800'
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          )
        })}
      </nav>

      {/* Sign out */}
      <div className="px-3 py-4 border-t border-slate-800">
        <button
          onClick={signOut}
          className="flex items-center gap-2 text-slate-500 hover:text-slate-300 text-sm w-full px-2 py-2 rounded-lg hover:bg-slate-800 transition-colors"
        >
          <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
          </svg>
          Sign out
        </button>
      </div>
    </div>
  )
}
